#include "libft.h"

void    *ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*s2;

	s2 = (char *)s;
	i = 0;
	while (i < n)
	{
		if (s2[i] == c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
